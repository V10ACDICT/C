/*******************************************************************************
整数を繰り返し入力する。ファイルの終わり(EOF)で入力を終了し、合計を出力する。
---例---
27  (27Enterと入力)
5
1
83
35
60  (ctrl+zでEOFが出る)
211 (合計値が出力される)
********************************************************************************/
#include <stdio.h>

int main (void){
  int seisu = 0;
  int goukei = 0;
  while( scanf("%d", &seisu) != EOF ){
    goukei += seisu;
  }

  printf("%d\n", goukei);
  return 0;
}
