/*******************************************************************************
1〜10までの整数の合計を計算し、合計を出力する。ただし計算にはfor文を使う。
********************************************************************************/
#include <stdio.h>

int main(void) {
  int goukei = 0;
  int i;

  for(i=1; i<=10; i++){
    goukei += i;
  }
  printf("1〜10までの合計は%dです。\n", goukei);
  return 0;
}
