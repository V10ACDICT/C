/********************************************************
標準入力より整数を5個入力し、入力順とは逆に標準出力に出力する。
---出力例---
整数==> 1
整数==> 2
整数==> 3
整数==> 4
整数==> 5
---逆順出力---
    5
    4
    3
    2
    1
*********************************************************/
#include <stdio.h>

int main(void) {
  int i, seisu[5];

  for ( i = 0; i < 5; i++) {
    printf("整数==> ");
    scanf("%d", &seisu[i]);
  }
  printf("---逆順出力---\n");
  for( i = 4; i >= 0; i--){
    printf("    %d\n", seisu[i]);
  }
  return 0;
}
