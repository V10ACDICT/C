/*******************************************************************************
整数を入力し、その階乗を出力する。
********************************************************************************/
#include <stdio.h>

int main(void) {
  int kaijo = 1, i, j, seisu;

  printf("整数＝＝＞ ");
  scanf("%d", &seisu);
  for ( i = 1;  i <= seisu; i++) {
    kaijo *= i;
  }
  printf("%dの階乗は%dです。\n", seisu, kaijo);
  return 0;
}
